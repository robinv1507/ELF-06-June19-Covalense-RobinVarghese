public class Product {

	int pid;
	String ptype;
	String name;
	double cost;

	public Product(int pid, String ptype, String name, double cost) {
		super();
		this.pid = pid;
		this.ptype = ptype;
		this.name = name;
		this.cost = cost;
	}//end of constructor

}//end of class
